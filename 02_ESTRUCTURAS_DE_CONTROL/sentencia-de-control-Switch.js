//sentencia de control Switch

let nota = 4;

switch (nota) {
    case 5:
        console.log("Excelente");
        break;
    case 4:
        console.log("Muy bien");
        break;
    case 3:
        console.log("Bien");
        break;
    case 2:
        console.log("Regular");
        break;
    case 1:
        console.log("Mal");
        break;
    default:
        console.log("Nota no valida");
        break;
}
